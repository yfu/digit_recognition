function arabic = word_to_arabic(word) 
    switch word
        case 'zero'
            arabic = 0;
        case 'one'
            arabic = 1;
        case 'two'
            arabic = 2;
        case 'three'
            arabic = 3;
        case 'four'
            arabic = 4;
        case 'five'
            arabic = 5;
        case 'six'
            arabic = 6;
        case 'seven'
            arabic = 7;
        case 'eight'
            arabic = 8;
        case 'nine'
            arabic = 9;            
    end
end